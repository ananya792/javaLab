package oopj;

public class swapIntc {

	public swapIntc() {}
		public static void main(String[] args) {
			int a = 5, b = 10;
			int c;
			System.out.println("int a is "+ a);
			System.out.println("int b is "+ b);
			//System.out.println("int c is "+ c);
			//swapping
			c = a;
			a = b;
			b = c;
			System.out.println("int a is "+ a);
			System.out.println("int b is "+ b);
			//System.out.println("int c is "+ c);
		}
	}


